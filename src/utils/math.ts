/**
 * 計算兩個數字的總和
 * @param a - 第一個數字
 * @param b - 第二個數字
 * @returns 兩個數字的總和
 */
export const sum = (a: number, b: number): number => {
  return a + b;
};

/**
 * 計算兩個數字的乘積
 * @param a - 第一個數字
 * @param b - 第二個數字
 * @returns 兩個數字的乘積
 */
export const multiply = (a: number, b: number): number => {
  return a * b;
};
